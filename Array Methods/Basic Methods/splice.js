// adding elements
const items2 = ['item1', 'item2', 'item3', 'item4', 'item5'];

items2.splice(5, 0, 'item6', 'item7');

console.log(items2);

// removing elements
const items1 = ['item1', 'item2', 'item3', 'item4', 'item5'];

items1.splice(2, 2);

console.log(items1);

